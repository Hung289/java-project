    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:  
        if (checkValidate()==0) {
            ServiceDaoImp serviceDaoImp = new ServiceDaoImp();
            Service service_entity = new Service(Integer.parseInt(txtId.getText()),txtName.getText(), Float.parseFloat(txtPrice.getText()));
            if (serviceDaoImp.update(service_entity)!=0) {
                DialogThongBao.showSuccess(this, "Alert", "Update data success");
                loadTable();
                btn_delete.setEnabled(false);
            }else{
                DialogThongBao.showError(this, "Alert", "Update data error!");
            }
        }
       
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        if (checkValidate()==0) {
            ServiceDaoImp serviceDaoImp = new ServiceDaoImp();
            Service service_entity = new Service(txtName.getText(), Float.parseFloat(txtPrice.getText()));
            if (serviceDaoImp.insert(service_entity)!=0) {
                DialogThongBao.showSuccess(this, "Alert", "Insert data success");
                loadTable();
            }else{
                 DialogThongBao.showError(this, "Alert", "Insert data success");
            }
        }
        
            
      
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        if (DialogThongBao.showAlert(this, "Alert", "Do you want to delete?")==0) {
            ServiceDaoImp serviceDaoImp = new ServiceDaoImp();
            serviceDaoImp.delete(Integer.parseInt(txtId.getText()));
            loadTable();
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void table_dataSericeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_dataSericeMouseClicked
        // TODO add your handling code here:
        int row = table_dataSerice.getSelectedRow();
        int id = (int) table_dataSerice.getValueAt(row,0);
//        Call class 
        ServiceDaoImp serviceDaoImp = new ServiceDaoImp();
        Service service_entity = serviceDaoImp.getServiceById(id);
//        Xet gia tri 
        txtName.setText(service_entity.getName());
        txtPrice.setText(String.valueOf(service_entity.getPrice()));
        txtId.setText(String.valueOf(service_entity.getId()));
//        set btn 
        btn_add.setEnabled(false);
        btn_delete.setEnabled(true);
        btn_edit.setEnabled(true);
        btn_delete.setEnabled(true);
    }//GEN-LAST:event_table_dataSericeMouseClicked


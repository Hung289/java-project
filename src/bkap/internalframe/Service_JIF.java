    public int checkValidate(){
        int checkValidate=0;
        //        Validate 
        if (txtName.getText().trim().length()==0) {
            checkValidate++;
            txtErrorName.setText("Name not empty!");
        }else{
            txtErrorName.setText("");
        }
        try {
            Float price = Float.parseFloat(txtPrice.getText());
            txtErrorPrice.setText("");
        } catch (NumberFormatException e) {
            checkValidate++;
            txtErrorPrice.setText("Price must number!");
        }
        return checkValidate;
    }
    public void loadTable(){
        String[] colums = {"Id","Name","Price","Date Created"};
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        defaultTableModel.setColumnIdentifiers(colums);
        ServiceDaoImp serviceDaoImp = new ServiceDaoImp();
        List<Service> ls =  serviceDaoImp.getAllService();
        for (Service l : ls) {
            defaultTableModel.addRow(new Object[]{
                l.getId(),
                l.getName(),
                l.getPrice(),
                l.getDateCreated()
            });
        }
        table_dataSerice.setModel(defaultTableModel);
    }

